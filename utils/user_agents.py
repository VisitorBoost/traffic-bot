def get_random_user_agent(config: Dict) -> str:
    """
    Select a random user agent based on the device distribution in the config.
    
    Args:
        config: The full bot configuration dictionary.
        
    Returns:
        A user agent string matching the probabilistic device distribution.
    """
    devices = config.get("devices", {})
    desktop_pct = devices.get("desktop_percent", 60)
    mobile_pct = devices.get("mobile_percent", 30)
    # tablet_pct is the remainder

    roll = random.randint(1, 100)

    if roll <= desktop_pct:
        return random.choice(DESKTOP_USER_AGENTS)
    elif roll <= desktop_pct + mobile_pct:
        return random.choice(MOBILE_USER_AGENTS)
    else:
        return random.choice(TABLET_USER_AGENTS)


def get_device_type_from_ua(user_agent: str) -> str:
    """Determine device type from user agent string."""
    ua_lower = user_agent.lower()
    if "ipad" in ua_lower or "tablet" in ua_lower:
        return "tablet"
    elif "mobile" in ua_lower or "android" in ua_lower or "iphone" in ua_lower:
        return "mobile"
    else:
        return "desktop"
